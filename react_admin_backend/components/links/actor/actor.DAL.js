const pool = require('../../../database/mysqlpool');

function createActor(name, user_create) {
    return new Promise(( resolve, reject ) => {
        pool.getConnection( (err, connection) => {
         if (err) {
          reject( err )
         } else {
            var values = {name};

            if (user_create)
              values = {name, user_create};

            var sql = 'INSERT INTO actor SET ?';
          connection.query(sql, values, ( err, rows) => {
           if ( err ) {
            reject( err )
           } else {
            resolve( rows )
           }
           connection.release()
          })
         }
        })
       })
}

